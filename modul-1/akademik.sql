CREATE DATABASE db_mahasiswa;
USE db_mahasiswa;

CREATE TABLE Mahasiswa (
  Nim VARCHAR (25) PRIMARY KEY,
  Nama VARCHAR(50),
  Jurusan VARCHAR(50),
  jenis_kelamin ENUM('Laki','Perempuan')
);
CREATE TABLE Dosen (
   Nip VARCHAR (25) PRIMARY KEY,
   Nama VARCHAR(50),
   Bidang_Studi VARCHAR(100)
);
CREATE TABLE MataKuliah (
    id_MataKuliah VARCHAR(10) PRIMARY KEY,
    Nip VARCHAR (25), 
    Nama_MataKuliah VARCHAR(100),
    SKS INT,
    FOREIGN KEY (Nip) REFERENCES Dosen(Nip) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE Krs (
    Id_Krs INT AUTO_INCREMENT PRIMARY KEY,
    Nim VARCHAR (25),
    id_MataKuliah VARCHAR(10),
    Semester INT,
    FOREIGN KEY (Nim) REFERENCES Mahasiswa(Nim),
    FOREIGN KEY (id_MataKuliah) REFERENCES MataKuliah(id_MataKuliah)
);
		

